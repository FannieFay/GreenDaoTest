package com.example.testactionbar.util;

public class BookPageConfiguration
{
    int size, margin, line_spacing, marginTop, width, height;

    public int getSize()
    {
        return size;
    }

    public BookPageConfiguration setSize(int size)
    {
        this.size = size;
        return this;
    }

    public int getMargin()
    {
        return margin;
    }

    public BookPageConfiguration setMargin(int margin)
    {
        this.margin = margin;
        return this;
    }

    public int getLine_spacing()
    {
        return line_spacing;
    }

    public BookPageConfiguration setLine_spacing(int line_spacing)
    {
        this.line_spacing = line_spacing;
        return this;
    }

    public int getMarginTop()
    {
        return marginTop;
    }

    public BookPageConfiguration setMarginTop(int marginTop)
    {
        this.marginTop = marginTop;
        return this;
    }

    public int getWidth()
    {
        return width;
    }

    public BookPageConfiguration setWidth(int width)
    {
        this.width = width;
        return this;
    }

    public int getHeight()
    {
        return height;
    }

    public BookPageConfiguration setHeight(int height)
    {
        this.height = height;
        return this;
    }

}
